// This is program calculates the area and perimeter of a rectangle
//     with dimensions 10cm x 3cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions" << std::endl;
    std::cout << "10cm x 3cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "The Area is: " << (10*3) << "cm^2" << std::endl;
    std::cout << "The Perimeter is: " << (2*(10+3)) << "cm" << std::endl;
}
